package ex_09_switch;

public class Lab06_Interview {
    public static void main(String[] args) {
        int a = 11;
        switch (-1) {

            default:
                System.out.println("Default");
                break;
            case -1:
                System.out.println("10");
                break;
            case 11:
                System.out.println("9");
                break;
        }
    }
}
