package ex_04_operators;

public class Lab_05_logical_operators {
    public static void main(String[] args) {

        // logical operators
        // && (AND)
        // || (OR)
        // ! (NOT) REVERSE

        boolean a = true;
        System.out.println(!a);

        boolean c = true || false;
        System.out.println(c);

        boolean c1 = false && true;
        System.out.println(c1);

        
    }


}
