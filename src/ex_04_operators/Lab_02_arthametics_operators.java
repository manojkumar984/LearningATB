package ex_04_operators;

public class Lab_02_arthametics_operators {
    public static void main(String[] args) {
        // arthametic operators
        // + addition
        // - subtraction
        // * multiplication
        // / division
        // % modulus
        int a = 20;
        int b = 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c);
        System.out.println("a+b");
        System.out.println(a+b);
    }
}
