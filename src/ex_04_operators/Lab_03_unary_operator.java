package ex_04_operators;

public class Lab_03_unary_operator {
    public static void main(String[] args) {
        // unary operator
        int a = 10;
        int a1 = -110;
        int result = a+a1;
        System.out.println(a);
        System.out.println(a1);
        System.out.println(result);

        int b = -1;
        b = b+1;
        System.out.println(b);
    }
}
