package ex_11_While_Loop;

public class Lab01_while_Loop {
    public static void main(String[] args) {
        int i = 0;                   //INT Part

        while (i<10){              // condition must be true to enter loop
            System.out.println(i);
            i++;                   // updation



           // ICU Model
            for (int j = 0; j < 10; j++) {
                System.out.println(j);
            }



        }
    }
}
