package ex_10_For_Loop;

public class Lab01_For_Loop {
    public static void main(String[] args) {
        // for loop
        // help you to repeat a block of code
        // initialization, condition, updation (ICU)
        // (I,C,u)

        for (int i = 0; i< 10; i++){
            System.out.println(i);
        }
    }
}
